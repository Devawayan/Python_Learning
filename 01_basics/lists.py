tea_varities = ["Black Tea" , "Green Tea" , "Oolong Tea" , "White Tea"]

print(tea_varities)

#Different Techniques in Lists that can be performed:

#find: 

print(tea_varities[1])

#Slicing

print(tea_varities[0:2])

#replace

tea_varities[3] = "Herbal Tea"

print(tea_varities)

tea_varities[1:2] = ["Lemon Tea"]
print(tea_varities)

tea_varities[1:3] = ["Green Tea" , "Masala Tea"]
print(tea_varities)

